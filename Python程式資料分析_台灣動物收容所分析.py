    print(f"🐕 Breed: {row['animal_Variety']} | Size: {row['animal_bodytype']} | Age: {row['animal_age']}")
    if row['album_file']:
        try:
            response = requests.get(row['album_file'], timeout=10)
            img = Image.open(BytesIO(response.content))
            plt.figure(figsize=(4, 3))
            plt.imshow(img)
            plt.axis('off')
            plt.title(f"ID: {row['animal_id']} | {row['days_waiting']} days waiting", fontsize=10)
            plt.tight_layout()
            plt.show()
        except Exception as e:
            print(f"📷 Photo unavailable: {e}")
    print("---")