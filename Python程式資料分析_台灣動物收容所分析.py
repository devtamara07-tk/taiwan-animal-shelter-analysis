plt.bar(xaxis, yaxis, color=['green', 'orange', 'red'])
plt.ylabel("Quantity")
plt.title("Kaohsiung Shelter Capacity vs Street Animals\n*External sources: news articles & government estimates")
plt.show()

#Animal size Vs Days Waiting boxplot:
sns.boxplot(data=df_kaohsiung, x='animal_bodytype', y='days_waiting')
plt.title("Size vs Waiting")
plt.show()

#Animals per city population
plt.bar(df_population['City'], df_population['animals_per_100k'], color=['green', 'orange', 'yellow', 'blue','red'])
plt.title("Animals per city population")
plt.ylabel("Animals per 100k")
plt.axhline(y=national_pop_mean, color='black', linestyle='--', label='National Average')
plt.legend()
plt.show()

# Round vets_per_100k to 2 decimal places
gdf_final['vets_per_100k'] = gdf_final['vets_per_100k'].round(2)

# Create base map centered on Kaohsiung
m = folium.Map(location=[22.6273, 120.3014], zoom_start=10)

# Add choropleth layer
folium.Choropleth(
    geo_data=gdf_final,
    data=gdf_final,
    columns=['town', 'vets_per_100k'],
    key_on='feature.properties.town',
    fill_color='YlOrRd',
    fill_opacity=0.7,
    line_opacity=0.5,
    legend_name='Vets per 100k Population'
).add_to(m)

# Add tooltips
folium.GeoJson(
    gdf_final,
    style_function=lambda x: {'fillOpacity': 0, 'weight': 0},
    tooltip=folium.GeoJsonTooltip(
        fields=['town', 'vets_per_100k', 'count'],
        aliases=['District:', 'Vets per 100k:', 'Total Vets:'],
    )
).add_to(m)

# Save map
m.save('kaohsiung_vet_map.html')
print("Map saved!")
m #display inline

